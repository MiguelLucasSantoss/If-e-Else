// 3) Desenvolva um programa que peça dois números ao usuário e informe qual é o maior e qual é o menor,
//  ou se são iguais.

n1 = Number(prompt("Digite um número: "))
n2 = Number(prompt("Digite outro número: "))

if(n1 > n2){
    alert ("O primeiro número é maior que o segundo")
}else{
    alert ("O segundo número é maior que o primeiro")
}